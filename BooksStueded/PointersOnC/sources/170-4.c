unsigned int Identity_matrix( int *chars, int make )
{
	int i, j;
	
	for( i = 0; i < make; i++ )
		for( j = 0; j < make; j++ ){
			if( i == j ){
				if( *chars != true )
					return false;
			}
			else if( *chars != false )
				return false;
			chars++;
		}
	
	return true;
}